package com.daria;

public class Vertex {
    //movement in left-right direction
    double x;
    //movement in up-down direction
    double y;
    //depth(movement towards/away user)
    double z;

    Vertex(double x, double y, double z){
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
